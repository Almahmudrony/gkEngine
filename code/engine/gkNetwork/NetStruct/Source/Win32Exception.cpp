///////////////////////////////////////////////////////////////////////////////
// CWin32Exception
///////////////////////////////////////////////////////////////////////////////

CWin32Exception::CWin32Exception(
   const CStringW &where, 
   DWORD error)
   :  CCommException(where, GetLastErrorMessage(error)),
      m_error(error)
{
}

DWORD CWin32Exception::GetError() const 
{ 
   return m_error; 
}

